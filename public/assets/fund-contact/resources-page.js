function renderFaq() {
  const tabsRoot = document.getElementById("resources-faq-tabs");
  const panelsRoot = document.getElementById("resources-faq-panels");

  if (!tabsRoot || !panelsRoot) {
    return;
  }

  tabsRoot.innerHTML = "";
  panelsRoot.innerHTML = "";

  resourcesFaqCategories.forEach((category, index) => {
    const button = document.createElement("button");
    button.type = "button";
    button.className = "resources-faq-tab";
    button.id = `faq-tab-${category.id}`;
    button.setAttribute("role", "tab");
    button.setAttribute("aria-controls", `faq-panel-${category.id}`);
    button.setAttribute("aria-selected", index === 0 ? "true" : "false");
    button.dataset.target = category.id;
    button.textContent = category.label;
    tabsRoot.appendChild(button);

    const panel = document.createElement("section");
    panel.className = "resources-faq-panel";
    panel.id = `faq-panel-${category.id}`;
    panel.setAttribute("role", "tabpanel");
    panel.setAttribute("aria-labelledby", button.id);
    panel.hidden = index !== 0;

    category.items.forEach((item, itemIndex) => {
      const details = document.createElement("details");
      details.className = "resources-faq-item";
      details.open = index === 0 && itemIndex === 0;

      const summary = document.createElement("summary");
      summary.textContent = item.question;

      const body = document.createElement("div");
      body.className = "resources-faq-answer";

      const paragraph = document.createElement("p");
      paragraph.textContent = item.answer;
      body.appendChild(paragraph);

      details.appendChild(summary);
      details.appendChild(body);
      panel.appendChild(details);
    });

    panelsRoot.appendChild(panel);
  });

  tabsRoot.addEventListener("click", (event) => {
    const button = event.target.closest(".resources-faq-tab");
    if (!button) {
      return;
    }

    const targetId = button.dataset.target;
    const buttons = tabsRoot.querySelectorAll(".resources-faq-tab");
    const panels = panelsRoot.querySelectorAll(".resources-faq-panel");

    buttons.forEach((tab) => {
      const isActive = tab === button;
      tab.setAttribute("aria-selected", isActive ? "true" : "false");
    });

    panels.forEach((panel) => {
      panel.hidden = panel.id !== `faq-panel-${targetId}`;
    });
  });
}

document.addEventListener("DOMContentLoaded", renderFaq);
